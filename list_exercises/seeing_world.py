least_places = ["Sambisa", "Bermuda", "Ijebu", "Ilesha", "Nembe"]

print("\nHere is the original list:")
print(least_places)

print("\nHere is the sorted list:")
print(sorted(least_places))

print("\nHere is the original list:")
print(least_places)

print("\nHere is the reversed sorted list:")
print(sorted(least_places, reverse=True))

print("\nHere is the original list:")
print(least_places)

least_places.reverse()
print("\nHere is the reversed list:")
print(least_places)

least_places.reverse()
print("\nHere is the reversed list again to original:")
print(least_places)

least_places.sort()
print("\nHere is the sorted list:")
print(least_places)

least_places.sort(reverse=True)
print("\nHere is the reversed sorted list:")
print(least_places)
